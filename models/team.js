//////////////////////////////////////////////
// Import Dependencies
//////////////////////////////////////////////
const mongoose = require("./connection")


////////////////////////////////////////////////
// Our Models
////////////////////////////////////////////////
// pull schema and model from mongoose
const {Schema, model} = mongoose

// team schema
const teamsSchema = new Schema({
    name: String,
    rings: String,
    bestPlayer: String,
    conference: String
})

// make model
const Team = model("Team", teamsSchema)


///////////////////////////////////////////////////
// Export Model
///////////////////////////////////////////////////
module.exports = Team